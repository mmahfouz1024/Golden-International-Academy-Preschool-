export const ThemeProvider: React.FC<{children: React.ReactNode}> = ({ children }) => {
  const [theme, setTheme] = useState<Theme>(() => {
    return (localStorage.getItem('golden_academy_theme') as Theme) || 'smart';
  });

  useEffect(() => {
    localStorage.setItem('golden_academy_theme', theme);
    const palette = THEME_PALETTES[theme];
    const root = document.documentElement;

    Object.entries(palette).forEach(([shade, color]) => {
      root.style.setProperty(`--color-primary-${shade}`, color as string);
    });

    // Update meta theme color
    document.querySelector('meta[name="theme-color"]')?.setAttribute('content', palette[600]);

  }, [theme]);

  return (
    <ThemeContext.Provider value={{ theme, setTheme }}>
      {children}
    </ThemeContext.Provider>
  );
};

export const useTheme = () => {
  const context = useContext(ThemeContext);
  if (!context) throw new Error('useTheme must be used within ThemeProvider');
  return context;
};
